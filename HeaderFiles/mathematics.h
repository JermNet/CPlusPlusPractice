#ifndef MATHEMATICS_H
#define MATHEMATICS_H
int add(int a, int b) {
    return a + b;
}

int sub(int a, int b) {
    return a - b;
}

int mul(int a, int b) {
    return a * b;
}

double divide(double a, double b) {
    return a / b;
}
#endif //MATHEMATICS_H
